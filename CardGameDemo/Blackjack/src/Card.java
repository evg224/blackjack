
public class Card 
{
	private int id;
	private String str;
	private int val;
	
	
	public Card(int id, String str, int val) 
	{
		this.id = id;
		this.str = str;
		this.val = val;
	}

	public int getId() 
	{
		return id;
	}
	
	public void setId(int id) 
	{
		this.id = id;
	}
	
	public String getStr() 
	{
		return str;
	}
	
	public void setStr(String str) 
	{
		this.str = str;
	}
	
	public int getVal() 
	{
		return val;
	}
	
	public void setVal(int val) 
	{
		this.val = val;
	}
	
	
	
}
